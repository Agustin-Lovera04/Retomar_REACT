//Realiza un programa donde ingreses 3 notas y el programa mcalcule y muestre en pantalla el promedio.


let nota1 = parseInt(prompt('Ingrese la nota 1: '))
let nota2 = parseInt(prompt('Ingrese la nota 2: '))
let nota3 = parseInt(prompt('Ingrese la nota 3: '))

let promedio = (nota1+nota2+nota3)/3

alert('El promedio de tus notas es: ' + promedio.toFixed(2))





